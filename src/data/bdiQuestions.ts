export interface BDIResult {
  level: "minimal" | "mild" | "moderate" | "severe";
  label: string;
  description: string;
  color: string;
  range: string;
}

export function getResult(score: number): BDIResult {
  if (score <= 13) {
    return {
      level: "minimal",
      label: "没有或极少抑郁",
      description:
        "您的评估结果显示目前没有明显的抑郁症状。请继续保持良好的心理状态，注意日常的情绪管理和自我关爱。",
      color: "success",
      range: "0-13",
    };
  } else if (score <= 19) {
    return {
      level: "mild",
      label: "轻度抑郁",
      description:
        "您的评估结果提示可能存在轻度抑郁情绪。建议您关注自身情绪变化，适当增加社交活动和运动，如持续感到困扰，建议咨询专业心理咨询师。",
      color: "warning",
      range: "14-19",
    };
  } else if (score <= 28) {
    return {
      level: "moderate",
      label: "中度抑郁",
      description:
        "您的评估结果提示可能存在中度抑郁。建议您尽快寻求专业心理咨询师或精神科医生的帮助，进行进一步的评估和干预。",
      color: "warning",
      range: "20-28",
    };
  } else {
    return {
      level: "severe",
      label: "重度抑郁",
      description:
        "您的评估结果提示可能存在重度抑郁。强烈建议您立即寻求精神科医生或心理危机干预热线的帮助。请记住，寻求帮助是勇敢的表现。",
      color: "destructive",
      range: "29-63",
    };
  }
}
